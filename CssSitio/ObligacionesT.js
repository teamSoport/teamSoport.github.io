$('.order1').on('shown.bs.collapse', function(){
    $("#sp1").on('shown.bs.tab').parent().find(".fa-plus").removeClass("fa-plus").addClass("fa-minus");
}).on('hidden.bs.collapse', function(){
    $("#sp1").on('shown.bs.tab').parent().find(".fa-minus").removeClass("fa-minus").addClass("fa-plus");
}).on('shown.bs.tab');

